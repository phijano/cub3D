#include <stddef.h>

int	filecheck(const char *s, size_t len)
{
	if (s[--len] == 'b')
		if (s[--len] == 'u')
			if (s[--len] == 'c')
				if (s[--len] == '.')
					return (0);
	return (-1);
}
